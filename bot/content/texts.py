from __future__ import annotations

_WELCOME_TEMPLATE = """\
Добро пожаловать в таверну "Тихое пламя".

Здесь не всегда есть с кем поболтать, но всегда можно отдохнуть. Таверна существует в разных мирах и измерениях. По сему не редко новый гость теряется завидев ее там, где ранее мог быть пустырь или непроходимый лес. И даже пугается, увидев и услышав незнакомых ему до сего момента существ. Но здесь рады всем, кто ведет себя достойно и не буянит.

Проходя в зал помни:
Каждый посетитель уникален, как и ты. Не каждый плох просто потому, что ведет себя непривычно.
Вежливость — универсальный инструмент для понимания друг друга.
Уважение к собеседнику — залог хорошего общения.
Недобросовестным посетителям могут сделать замечание или вынести предупреждение.
А буйных могут и выгнать.
Не заходи за рамки устоявшихся моральных норм — и проблем у тебя не будет.

В таверне ты наверняка найдешь себе место.
За общим столом можно присоединиться к чьей-либо беседе.
За прилавком задать вопрос владельцу или хранителю.
Если есть что предложить — можешь оставить записку на доске идей и предложений.
Если интересна компания за столиком — возможно у них есть свободное местечко.
В тематических зонах, как и за столиком, можно:
Попробовать найти собеседника.
Поискать, с кем сыграть.
Найти себе компанию для общения.
Или спрятаться и поболтать наедине.
Можно просто подсесть рядом и послушать, о чем идет речь. Вряд ли тут кто-то будет сильно против.
Порой это может помочь прийти в себя, расслабиться, найти себе дело.

А если вдруг вы всё же нашли друг друга, и ну никак не можете не выяснить отношения, то милости просим на задний двор. Все отношения выясняем там. За драки и дебош любой будет наказан.

А теперь проходи. Удачи!

И возьми этот брелок, на память. В час нужды он приведет владельца ко входу в таверну.
{invite_url}"""


def format_welcome(invite_url: str) -> str:
    return _WELCOME_TEMPLATE.format(invite_url=invite_url)
